import { usePlantStore } from '../stores/plant'
import { format } from 'date-fns'
import { ElMessage, ElMessageBox } from 'element-plus'
import { Delete } from '@element-plus/icons-vue'

export default {
  name: 'PlantChatPage',
  setup() {
    const plantStore = usePlantStore()
    const messagesList = ref(null)
    const messageInput = ref('')
    const loading = ref(false)
    const showSuggestions = ref(true)
    
    // Âª∫ËÆÆÈóÆÈ¢ò
    const suggestions = [
      '‰Ω†‰ªäÂ§©ÊÑüËßâÊÄé‰πàÊ†∑Ôºü',
      'Êúâ‰ªÄ‰πàÁîüÈïøÂ∞èÁßòËØÄÂêóÔºü',
      '‰Ω†ÂñúÊ¨¢‰ªÄ‰πàÊ†∑ÁöÑÁéØÂ¢ÉÔºü',
      'Â¶Ç‰ΩïËÆ©‰Ω†Êõ¥Âø´ÊàêÈïøÔºü'
    ]
    
    // ÈªòËÆ§Ê¨¢ËøéËØ≠
    const defaultWelcomeMessage = {
      id: 'welcome',
      sender: 'plant',
      content: '‰Ω†Â•ΩÔºÅÊàëÊòØ‰Ω†ÁöÑÊ§çÁâ©‰ºô‰º¥ÔºåÂæàÈ´òÂÖ¥ËÉΩÂíå‰Ω†ËÅäÂ§©„ÄÇ‰Ω†ÂèØ‰ª•ÈóÆÊàë‰ªª‰ΩïÈóÆÈ¢òÔºåÊàñËÄÖÂàÜ‰∫´‰Ω†ÁöÑÊÉ≥Ê≥ï„ÄÇ',
      timestamp: new Date()
