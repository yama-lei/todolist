        if (!plantStore.currentPlant) return;
        
        const plantId = plantStore.currentPlant._id || plantStore.currentPlant.id;
        try {
          // Ë∞ÉÁî®Ê∏ÖÁ©∫ÂØπËØùÁöÑAPI
